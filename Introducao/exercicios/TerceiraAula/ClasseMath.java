package Introducao.exercicios.TerceiraAula;

public class ClasseMath {
    public static void main(String[] args) {
        double pi = Math.PI; //Valor de pi
        System.out.println(pi);
        System.out.println(Math.pow(5, 2)); //5^2
        System.out.println(Math.sqrt(25)); 

        System.out.println(Math.abs(-10)); //módulo
        System.out.println(Math.floor(3.9)); //funcao piso
        System.out.println(Math.ceil(4.2)); //funcao teto
        System.out.println(Math.round(5.6)); //arredondamento
        System.out.println(Math.random()); //gera um numero x aleatorio 0<x<1
    }
    
}
